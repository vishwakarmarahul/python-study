brands = ['suzuki', 'kia', 'bmw']

#index example
car_name = brands[0]
print(car_name)

car_name = brands[1]
print(car_name)

car_name = brands[2]
print(car_name)

print('negative indexing')
car_name = brands[-1]
print(car_name)

car_name = brands[-2]
print(car_name)

car_name = brands[-3]
print(car_name)

#length
print(len(brands))

